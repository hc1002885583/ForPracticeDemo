public class ForDemo2 {
    public static void main(String[] args) {
        for (int i = 1; i <= 5; i++) {
            System.out.println(i);
        }
        System.out.println("----------------------");
        for (int i = 1; i <= 5; i++) {
            System.out.println(6 - i);
        }
    }
}
